# Suma

r = 15 + 23

# Resta 

r2 = 30 - 20

# Multiplicación

r3 = 5 * 10

# División 

r4 = 200 / 4

# División entera
r5 = 200 // 4

# Modulo  'El modulo es el restante de la division de un numero que no puede ser dividido en partes exactas enteras.

r6 = 15 % 2

# Exponente 

r7 = 5**2

print('La suma es {}'.format(r))
print('La resta es {}'.format(r2))
print('La multiplicación es {}'.format(r3))
print('La división es {}'.format(r4))
print('La división en forma entera es {}'.format(r5))
print('El modulo es {}'.format(r6))
print('El exponente es {}'.format(r7))

